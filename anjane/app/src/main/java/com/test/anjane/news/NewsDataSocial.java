package com.test.anjane.news;

public class NewsDataSocial {
    private String titleSocial;
    private String timeSocial;
    private String urlSocial;
    private String imageSocial;

    public String getTitleSocial() {
        return titleSocial;
    }

    public void setTitleSocial(String titleSocial) {
        this.titleSocial = titleSocial;
    }

    public String getTimeSocial() {
        return timeSocial;
    }

    public void setTimeSocial(String timeSocial) {
        this.timeSocial = timeSocial;
    }

    public String getUrlSocial() {
        return urlSocial;
    }

    public void setUrlSocial(String urlSocial) {
        this.urlSocial = urlSocial;
    }

    public String getImageSocial() {
        return imageSocial;
    }

    public void setImageSocial(String imageSocial) {
        this.imageSocial = imageSocial;
    }
}